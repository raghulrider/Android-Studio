package com.raghulrider.thesmartflow;

public class PumpInformation {
    private String basal;
    private String batterylevel;
    private String bolus;
    private String email;
    private String patientID;
    private String pin;
    private String reservoirlevel;
    private String username;
    private String age;
    private String weight;
    private String gender;
    private String diabetestype;

    public PumpInformation() {
    }

    public String getBasal() {
        return basal;
    }

    public void setBasal(String basal) {
        this.basal = basal;
    }

    public String getBatterylevel() {
        return batterylevel;
    }

    public void setBatterylevel(String batterylevel) {
        this.batterylevel = batterylevel;
    }

    public String getBolus() {
        return bolus;
    }

    public void setBolus(String bolus) {
        this.bolus = bolus;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPatientID() {
        return patientID;
    }

    public void setPatientID(String patientID) {
        this.patientID = patientID;
    }

    public String getPin() {
        return pin;
    }

    public void setPin(String pin) {
        this.pin = pin;
    }

    public String getReservoirlevel() {
        return reservoirlevel;
    }

    public void setReservoirlevel(String reservoirlevel) {
        this.reservoirlevel = reservoirlevel;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getDiabetestype() {
        return diabetestype;
    }

    public void setDiabetestype(String diabetestype) {
        this.diabetestype = diabetestype;
    }
}
